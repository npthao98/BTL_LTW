package model;

public class Type {
	private int ID;
	private String Name;
	public Type() {
		super();
	}
	public Type(int iD, String name) {
		super();
		ID = iD;
		Name = name;
	}
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	
	
}
